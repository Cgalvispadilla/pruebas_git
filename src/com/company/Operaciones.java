package com.company;

public class Operaciones {

    public double suma(int num1, int num2 ){
        return num1 + num2;
    }

    public double resta(int num3, int num4 ){
        return num3 - num4;
    }

    public double multiplicacion(int num3, int num4 ){
        return num3 * num4;
    }
}
